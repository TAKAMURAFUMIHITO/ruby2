# div 割り算の商（整数）
p 5.div(2)
p 5.div(2.2)
p -5.div(2)
p -5.div(2.2)

# quo 割り算の商
p 5.quo(2)
p 5.quo(2.2)
p -5.quo(2)
p -5.quo(2.2)

# modulo 割り算の余り
p 5.modulo(2)
p 5.modulo(2.2)
p -5.modulo(2)
p -5.modulo(2.2)

# divmod 割り算の商と余りを配列で表示
p 10.divmod(3.5)
p 10.divmod(-3.5)
p -10.divmod(3.5)
p -10.divmod(-3.5)